    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if(txtId.getText().equals("")){
            txtId.setText(String.valueOf(LocadoraDados.setIdAuto()));
        }

        Cliente cli = new Cliente();
        String id, nome, endereco, telefone;
        boolean dadosValidos;

        try{
            cli = new Cliente(txtNome.getText(), txtId.getText(), txtEndereco.getText(), txtTelefone.getText());
            dadosValidos = true;
        }catch(java.lang.IllegalArgumentException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
            dadosValidos = false;
        }

        if(getIndice() == -1){
           

            if(dadosValidos == true){
                lblIndice.setText(String.valueOf(LocadoraDados.getClientes().size()));
                LocadoraDados.cadastraCliente(cli);

            }
        }else{
            
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja alterar os dados?", "Confirmar ação", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                
                
                LocadoraDados.alteraCliente(getIndice(), cli);
            }else{
                preencheCampos(getIndice());
            }
        }

        //lblIndice.setText(String.valueOf(LocadoraDados.getClientes().size()-1));
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        if(lblIndice.getText().equals("(Novo)")){
            JOptionPane.showMessageDialog(null, "Não há nada para excluir");
        }else{

            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir esse registro?", "Receber", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {

                int minIndice = 0, maxIndice, indice;
                maxIndice = LocadoraDados.getClientes().size()-1;
                
                try{
                    indice = Integer.parseInt(lblIndice.getText());
                }catch(NumberFormatException e){
                    indice = maxIndice;
                }
                if((indice >= 0) && (indice <= maxIndice)){

                    if(maxIndice>0 && indice < maxIndice){

                        LocadoraDados.removeCliente(indice);
                        preencheCampos(indice);
                    }else if((indice == 0) && (maxIndice == 0)){

                        LocadoraDados.removeCliente(indice);
                        limpaCampos();
                    }else if((indice == 0) && (maxIndice > 0)){

                        LocadoraDados.removeCliente(indice);
                        preencheCampos(indice);
                    }else if((indice == maxIndice) && (maxIndice > 0)){

                        LocadoraDados.removeCliente(indice);
                        indice --;
                        preencheCampos(indice);
                    }
                }
            }else{

            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        int indice;
        try{
            indice = Integer.parseInt(lblIndice.getText());
        }catch(java.lang.NumberFormatException e){
            indice = LocadoraDados.getClientes().size();
        }

        if (LocadoraDados.getClientes().size() == 0){
            JOptionPane.showMessageDialog(null, "Esse é o primeiro registro");
        }else{

            if(indice <= 0){
                JOptionPane.showMessageDialog(null, "Esse é o primeiro registro");
            }else{
                indice --;
                preencheCampos(indice);  //Chama o metodo para preencher os campos
            }

        }

    }//GEN-LAST:event_btnAnteriorActionPerformed

    private void btnProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProximoActionPerformed
        int indice;
        try{

            indice = Integer.parseInt(lblIndice.getText());

        }catch(java.lang.NumberFormatException e){
            indice = LocadoraDados.getClientes().size();
        }

        if(indice+1 >= LocadoraDados.getClientes().size()){
            JOptionPane.showMessageDialog(null, "Esse é o ultimo registro");
        }else{
            indice++;
            preencheCampos(indice);
        }

    }//GEN-LAST:event_btnProximoActionPerformed

