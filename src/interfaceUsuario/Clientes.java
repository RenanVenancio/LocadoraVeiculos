    private void preencheCampos(int indice){
        if(indice < 0){
        }else{
            txtId.setText(String.valueOf(LocadoraDados.getClientes().get(indice).getID()));
            txtNome.setText(String.valueOf(LocadoraDados.getClientes().get(indice).getNome()));
            txtEndereco.setText(String.valueOf(LocadoraDados.getClientes().get(indice).getEndereco()));
            txtTelefone.setText(String.valueOf(LocadoraDados.getClientes().get(indice).getTel()));
            lblIndice.setText(String.valueOf(indice));
        }
    }
    
    
    private void limpaCampos(){
        txtId.setText("");
        txtNome.setText("");
        txtEndereco.setText("");
        txtTelefone.setText("");
        lblIndice.setText("(Novo)");
    }
    
    private int getIndice(){
        int indice = -1;
        try{
            indice = Integer.parseInt(lblIndice.getText());
        }catch(NumberFormatException e){
            
            return -1;
        }
        return indice;
    }

